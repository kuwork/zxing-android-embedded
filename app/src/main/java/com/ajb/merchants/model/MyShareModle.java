package com.ajb.merchants.model;

public class MyShareModle 
{
	private String Id;
	private String parking_name;
	private String parking_price;
	private String state;
	private String carport_no;
	private String ltd_code;
	private String park_code;
	private String reason;
	
	
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getCarport_no() {
		return carport_no;
	}
	public void setCarport_no(String carport_no) {
		this.carport_no = carport_no;
	}
	public String getLtd_code() {
		return ltd_code;
	}
	public void setLtd_code(String ltd_code) {
		this.ltd_code = ltd_code;
	}
	public String getPark_code() {
		return park_code;
	}
	public void setPark_code(String park_code) {
		this.park_code = park_code;
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getParking_name() {
		return parking_name;
	}
	public void setParking_name(String parking_name) {
		this.parking_name = parking_name;
	}
	public String getParking_price() {
		return parking_price;
	}
	public void setParking_price(String parking_price) {
		this.parking_price = parking_price;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
		
}
