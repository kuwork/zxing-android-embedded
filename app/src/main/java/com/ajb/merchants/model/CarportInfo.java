package com.ajb.merchants.model;

public class CarportInfo {
	private String startTime;
	private String endTime;
	private String shareWay;
	private String shareId;
	private String lowestAmount;
	private String carportNo;
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getShareWay() {
		return shareWay;
	}
	public void setShareWay(String shareWay) {
		this.shareWay = shareWay;
	}
	public String getShareId() {
		return shareId;
	}
	public void setShareId(String shareId) {
		this.shareId = shareId;
	}
	public String getLowestAmount() {
		return lowestAmount;
	}
	public void setLowestAmount(String lowestAmount) {
		this.lowestAmount = lowestAmount;
	}
	public String getCarportNo() {
		return carportNo;
	}
	public void setCarportNo(String carportNo) {
		this.carportNo = carportNo;
	}
	
	

}
